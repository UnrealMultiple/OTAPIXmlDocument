namespace Microsoft.Xna.Framework;
/// <summary>二维向量</summary>
public struct Vector2
{
    /// <summary>X</summary>
    public float X;
    /// <summary>Y</summary>
    public float Y;
    /// <summary></summary>
    /// <param name="x"></param>
    /// <param name="y"></param>
    public Vector2(float x, float y) => throw null;
    /// <summary>X = value; Y = value</summary>
    /// <param name="value"></param>
    public Vector2(float value) => throw null;
    /// <summary>距离(三角形斜边长度)</summary>
    /// <param name="value1"></param>
    /// <param name="value2"></param>
    /// <returns>距离</returns>
    public static float Distance(Vector2 value1, Vector2 value2) => throw null;
}
